    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TO Find Record Button Option
        int sID = Integer.valueOf(JOptionPane.showInputDialog(this, "Enter Staff ID"));
        Staff s = sfDB.findRecord(sID);
        if (s != null) {
            txtStaffID.setText(String.valueOf(s.getStaffID()));
            txtLastName.setText(s.getLastName());
            
            // Set radio buttons
            if (s.getGender().equals("Male")) {
                rdoMale.setSelected(true);
            } else if (s.getGender().equals("Female")) {
                rdoFemale.setSelected(true);
            } 
            
            txtContactNumber.setText(String.valueOf(s.getContactNumber()));
            cmbDepartment.setSelectedItem(s.getDepartment());
            txtHireDate.setText(s.getHireDate().toString());
            txtSalary.setText(s.getSalary());
            cmbWorkSchedule.setSelectedItem(s.getWorkSchedule());
            cmbQualification.setSelectedItem(s.getQualifications());
            cmbStaffType.setSelectedItem(s.getStaffType());

            JOptionPane.showMessageDialog(rootPane, "Staff Record is Found");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Staff Record is Not Found");
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (checkVal()){
            int staffID = Integer.valueOf(txtStaffID.getText());
            String lastName = txtLastName.getText();
            
            String gender="";
            if(rdoMale.isSelected()) {
                gender="Male";
            } else if (rdoFemale.isSelected()){
                gender="Female";
            } else {
                gender="";
            }
            
            int contactNumber = Integer.valueOf(txtContactNumber.getText());
            String department = cmbDepartment.getSelectedItem().toString();
            Date hireDate=Date.valueOf(txtHireDate.getText());
            String salary=txtSalary.getText();
            String workSchedule = cmbWorkSchedule.getSelectedItem().toString();
            String qualifications = cmbQualification.getSelectedItem().toString();
            String staffType = cmbStaffType.getSelectedItem().toString();

            Stf = new Staff (staffID, lastName, gender, contactNumber, department, hireDate, salary, workSchedule, qualifications, staffType);
            if (sfDB.updateRecord(Stf))                
              JOptionPane.showMessageDialog(rootPane, "The Appointment Record is updated");
            else
               JOptionPane.showMessageDialog(rootPane, "The Appointment Record is not updated");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

