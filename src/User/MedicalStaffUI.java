    private boolean checkVal(){
        
        if (txtStaffID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Staff ID cannot be blank");
            return false;
        }
        if (txtLastName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Last Name cannot be blank");
            return false;
        }
         if (txtContactNumber.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Contact Number cannot be blank");
            return false;
        }   
        if (txtHireDate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Hire Date cannot be blank");
            return false;
        }
        if (txtSalary.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Salary cannot be blank");
            return false;
        }
        try{
             Date hireDate=Date.valueOf(txtHireDate.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Hire Date must be YYYY-MM-DD");
            return false;
        }  
        return true;
    }

