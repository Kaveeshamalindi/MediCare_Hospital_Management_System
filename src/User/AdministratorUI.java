    private void btnAddDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDataActionPerformed
        // TODO add your handling code here:
        if (checkVal()){
            int adminID = Integer.valueOf(txtAdminID.getText());
            String lastName = txtLastName.getText();
            
            String gender="";
            if(rdoMale.isSelected()) {
                gender="Male";
            } else if (rdoFemale.isSelected()){
                gender="Female";
            } else {
                gender="";
            } 
            
            String address = txtAddress.getText();
            String accessLevel = cmbAccessLevel.getSelectedItem().toString();
            String department = cmbDepartment.getSelectedItem().toString();
            Date hireDate=Date.valueOf(txtHireDate.getText());
            String adminType = cmbAdminType.getSelectedItem().toString();

            admin = new Administrator (adminID, lastName, gender, address, accessLevel, department, hireDate, adminType);
            if (AfDB.addAdmin(admin))           
              JOptionPane.showMessageDialog(rootPane, "The Admin Record is added");
            else
               JOptionPane.showMessageDialog(rootPane, "The Admin Record is not added");
        }
    }//GEN-LAST:event_btnAddDataActionPerformed

