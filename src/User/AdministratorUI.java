    private void btnDisplayDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayDataActionPerformed
        // TO Display Data Button option
        ArrayList<Administrator> AList=AfDB.getAll();
        tblModel.setRowCount(0);
        for(Administrator admin:AList){
            int AID= admin.getAdminID();
            String lastName = admin.getLastName();
            String gender = admin.getGender();
            String address = admin.getAddress();
            String accessLevel= admin.getAccessLevel();
            String department = admin.getDepartment();
            Date hireDate = admin.getHireDate();
            String adminType= admin.getAdminType();
            
            tblModel.addRow(new Object[]{AID, lastName,gender,address,accessLevel,department,hireDate,adminType});
        } 
    }//GEN-LAST:event_btnDisplayDataActionPerformed

    private void btnFindDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindDataActionPerformed
        // TO Find Data Button option
        int AID = Integer.valueOf(JOptionPane.showInputDialog(this, "Enter Admin ID"));
        Administrator A = AfDB.findAdmin(AID);
        if (A != null) {
            txtAdminID.setText(String.valueOf(A.getAdminID()));
            txtLastName.setText(A.getLastName());
            
            // Set radio buttons
            if (A.getGender().equals("Male")) {
                rdoMale.setSelected(true);
            } else if (A.getGender().equals("Female")) {
                rdoFemale.setSelected(true);
            }
            
            txtAddress.setText(A.getAddress());
            cmbAccessLevel.setSelectedItem(A.getAccessLevel());
            cmbDepartment.setSelectedItem(A.getDepartment());
            txtHireDate.setText(A.getHireDate().toString());
            cmbAdminType.setSelectedItem(A.getAdminType());
            
            JOptionPane.showMessageDialog(rootPane, "Patient is Found");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Patient is Not Found");
        }
    }//GEN-LAST:event_btnFindDataActionPerformed

