    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TO Add Button Option
        if (checkVal()){ // stop if validation fails
            int recordID = Integer.valueOf(txtRecordID.getText());
            String patient = txtPatientID.getText();
            String doctor = cmbDoctorName.getSelectedItem().toString();
            Date visitDate=Date.valueOf(txtVisitDate.getText());
            String symptoms = txtSymptoms.getText();
            String diagnosis=txtDiagnosis.getText();
            String treatment=txtTreatment.getText();
            String prescription=txtPrescription.getText();

            String testResult="";
            if(rdoInProgress.isSelected()) {  
                testResult="In Progress";
            } else if (rdoPending.isSelected()){
                testResult="Pending";
            } else if (rdoCompleted.isSelected()){
                testResult="Completed";
            } else {
                testResult="";
            }

            medRd = new MedicalRecord(recordID, patient, doctor, visitDate, symptoms, diagnosis, treatment, prescription, testResult);
            if (mRfDB.createRecord(medRd))           
              JOptionPane.showMessageDialog(rootPane, "Medical Record is added");
            else
               JOptionPane.showMessageDialog(rootPane, "Medical Record is not added");
        }
    }//GEN-LAST:event_btnAddActionPerformed

