    private boolean checkVal(){
        
        if (txtRecordID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Record ID cannot be blank");
            return false;
        }
        if (txtPatientID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Patient ID cannot be blank");
            return false;
        }
        if (txtVisitDate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Visit Date cannot be blank");
            return false;
        }
        if (txtSymptoms.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Symptoms cannot be blank");
            return false;
        }
        if (txtDiagnosis.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Diagnosis cannot be blank");
            return false;
        }
        if (txtTreatment.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Treatment cannot be blank");
            return false;
        }
        if (txtPrescription.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Prescription cannot be blank");
            return false;
        }
        if (!rdoInProgress.isSelected() && !rdoPending.isSelected() && !rdoCompleted.isSelected()) {
            JOptionPane.showMessageDialog(this, "Select Test Result");
            return false;
        }
        try{
             Date visitDate=Date.valueOf(txtVisitDate.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Visit Date must be YYYY-MM-DD");
            return false;
        }
        return true;
    }


