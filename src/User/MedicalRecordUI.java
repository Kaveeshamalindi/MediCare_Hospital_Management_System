    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        // TO Display Button Option
        ArrayList<MedicalRecord> mList=mRfDB.getAll();
        tblModel.setRowCount(0);
        for(MedicalRecord medRd:mList){
            int mRID= medRd.getRecordID();
            String patient= medRd.getPatient();
            String doctor = medRd.getDoctor();
            Date visitDate = medRd.getVisitDate();
            String symptoms = medRd.getSymptoms();
            String diagnosis = medRd.getDiagnosis();
            String treatment = medRd.getTreatment();
            String prescription = medRd.getPrescription();
            String testResult = medRd.getTestResult();
            tblModel.addRow(new Object[]{mRID, patient, doctor, visitDate, symptoms, diagnosis, treatment, prescription, testResult});
        } 
    }//GEN-LAST:event_btnDisplayActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TO Find Button Option
        int mRID = Integer.valueOf(JOptionPane.showInputDialog(this, "Enter Medical Record ID"));
        MedicalRecord mR = mRfDB.findRecord(mRID);
        if (mR != null) {
            txtRecordID.setText(String.valueOf(mR.getRecordID()));
            txtPatientID.setText(mR.getPatient());
            cmbDoctorName.setSelectedItem(mR.getDoctor());
            txtVisitDate.setText(mR.getVisitDate().toString());
            txtSymptoms.setText(mR.getSymptoms());
            txtDiagnosis.setText(mR.getDiagnosis());
            txtTreatment.setText(mR.getTreatment());
            txtPrescription.setText(mR.getPrescription());

            // Set radio buttons
            if (mR.getTestResult().equals("In Progress")) {
                rdoInProgress.setSelected(true);
            } else if (mR.getTestResult().equals("Pending")) {
                rdoPending.setSelected(true);
            } else if (mR.getTestResult().equals("Completed")) {
                rdoCompleted.setSelected(true);
            }

            JOptionPane.showMessageDialog(rootPane, "Medical Record is Found");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Medical Record is Not Found");
        }
    }//GEN-LAST:event_btnFindActionPerformed

