    private void btnSendReminderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendReminderActionPerformed
        // TO Send Reminder Button Option
        try {
        int aID = Integer.parseInt(
                JOptionPane.showInputDialog(this, "Enter Appointment ID to send reminder")
        );

        Appointment a = afDB.findAppointment(aID);

        if (a != null) {
            String message =
                    "** Appointment Reminder **\n\n" +
                    "Appointment ID: " + a.getAppointmentID() + "\n" +
                    "Patient: " + a.getPatient() + "\n" +
                    "Doctor: " + a.getDoctor() + "\n" +
                    "Date: " + a.getAppointmentDate() + "\n" +
                    "Time: " + a.getAppointmentTime() + "\n" +
                    "Status: " + a.getStatus()+ "\n" +
                    "Notes: " + a.getNotes();

            JOptionPane.showMessageDialog(
                    this,
                    message,
                    "Reminder Sent",
                    JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            JOptionPane.showMessageDialog(this, "Appointment not found");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Invalid Appointment ID");
      }
    }//GEN-LAST:event_btnSendReminderActionPerformed

