    private void btnFindAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindAppointmentActionPerformed
        // TO Find Appointment Button Option
        int aID = Integer.valueOf(JOptionPane.showInputDialog(this, "Enter Appointment ID"));
        Appointment a = afDB.findAppointment(aID);
        if (a != null) {
            txtAppointmentID.setText(String.valueOf(a.getAppointmentID()));
            txtPatientID.setText(a.getPatient());
            txtDoctorName.setText(a.getDoctor());
            txtAppointmentDate.setText(a.getAppointmentDate().toString());
            txtAppointmentTime.setText(a.getAppointmentTime());
            cmbType.setSelectedItem(a.getType());
            txtNotes.setText(a.getNotes());

            // Set radio buttons
            if (a.getStatus().equals("Confirmed")) {
                rdoConfirmed.setSelected(true);
            } else if (a.getStatus().equals("Rescheduled")) {
                rdoRescheduled.setSelected(true);
            } else if (a.getStatus().equals("Pending")) {
                rdoPending.setSelected(true);
            }

            JOptionPane.showMessageDialog(rootPane, "Appointment is Found");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Appointment is Not Found");
        }
    }//GEN-LAST:event_btnFindAppointmentActionPerformed

    private void btnReschduleAppointmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReschduleAppointmentActionPerformed
        // TO Reschdule Appointment Button Option
        if (checkVal()){
            int appointmentID = Integer.valueOf(txtAppointmentID.getText());
            String patient = txtPatientID.getText();
            String doctor = txtDoctorName.getText();
            Date appointmentDate=Date.valueOf(txtAppointmentDate.getText());
            String appointmentTime=txtAppointmentTime.getText();
            
            String type = cmbType.getSelectedItem().toString();

            String status="";
            if(rdoConfirmed.isSelected()) {
                status="Confirmed";
            } else if (rdoRescheduled.isSelected()){
                status="Rescheduled";
            } else if (rdoPending.isSelected()){
                status="Pending";
            } else {
                status="";
            }
            
            String notes=txtNotes.getText();

            app = new Appointment (appointmentID, patient, doctor, appointmentDate, appointmentTime, type, status, notes);
            if (afDB.rescheduleAppointment(app))           
              JOptionPane.showMessageDialog(rootPane, "The Appointment Record is updated");
            else
               JOptionPane.showMessageDialog(rootPane, "The Appointment Record is not updated");
        }
    }//GEN-LAST:event_btnReschduleAppointmentActionPerformed

