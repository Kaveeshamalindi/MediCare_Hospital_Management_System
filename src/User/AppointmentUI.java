    private boolean checkVal(){
        
        if (txtAppointmentID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Appointment ID cannot be blank");
            return false;
        }
        if (txtPatientID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Patient ID cannot be blank");
            return false;
        }
        if (txtDoctorName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Doctor Name cannot be blank");
            return false;
        }
        if (txtAppointmentDate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Appointment Date cannot be blank");
            return false;
        }
        if (txtAppointmentTime.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Appointment Time cannot be blank");
            return false;
        }
        if (!rdoConfirmed.isSelected() && !rdoRescheduled.isSelected() && !rdoPending.isSelected()) {
            JOptionPane.showMessageDialog(this, "Select appointment status");
            return false;
        }
        if (txtNotes.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Notes cannot be blank");
            return false;
        }
        try{
             Date appointmentDate=Date.valueOf(txtAppointmentDate.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Appointment Date must be YYYY-MM-DD");
            return false;
        }
        
        return true;
    }

